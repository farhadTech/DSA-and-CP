import java.util.*;
public class PrettyPrinting{
    public static void main(String[] args) {
        float a = 453.1234f;
        System.out.printf("Formatted number is %.2f\n", a);
        System.out.printf("Pie: %.3f\n", Math.PI);
        System.out.printf("Hello My name is %s and I am %s", "Kunal", "Cool");
    }
}
